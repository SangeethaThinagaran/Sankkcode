package Assignments;

public class StaticMethodOpertions4 
{
	static void add() 
	{
		System.out.println("Addition");
	}
	static void sub() 
	{
		System.out.println("Subtraction");
	}
	static void mul() 
	{
		System.out.println("Mutiplication");
	}
	static void div() 
	{
		System.out.println("Division");
	}
	
	public static void main(String[] args) 
	{
		add();
		sub();
		mul();
		div();
	}

}
