package javaprogramming;

public class IfElseBlock {
	public static void main(String[] args) {
		int age=3;
		if(age>18) {
			System.out.println("I can VOTE");
		}
		else {
			System.out.println("I can't VOTE");
		}
		
	// this statement will be executed irrespective of the IFELSE conditional block	
		
		System.out.println("Votings will be opened till 6PM");
	}

}
